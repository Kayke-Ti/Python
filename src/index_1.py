# Exemplo de estrutura condicional if-else
idade = 18
if idade >= 18:
    print("Você é maior de idade.")
else:
    print("Você é menor de idade.")

# Exemplo de estrutura de repetição while
contador = 0
while contador < 5:
    print("Contagem:", contador)
    contador += 1

# Exemplo de estrutura de repetição for
frutas = ["maçã", "banana", "laranja"]
for fruta in frutas:
    print("Eu gosto de", fruta)

# Exemplo de lista
numeros = [1, 2, 3, 4, 5]

# Exemplo de acesso a elementos da lista
print("Primeiro número:", numeros[0])
print("Tamanho da lista:", len(numeros))
